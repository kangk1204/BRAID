"""S6 Fig: confidence-tier validation on the TRA2 RT-PCR panel.

Reads benchmarks/results/per_tier_validation.json (generated by
benchmarks/headtohead/per_tier_validation.py) and draws two panels:
  (A) RT-PCR positive rate per tier (the tiers stratify orthogonal positivity:
      high-confidence highest, not-significant lowest), with the panel base rate.
  (B) RT-PCR coverage per tier (the calibrated interval stays near nominal 0.95 in
      every tier, not only marginally).

Output: paper/figures/supp_fig6_per_tier_validation.{png,pdf,svg} (+ outputs/ mirror).
"""
from __future__ import annotations

import json
import os
import sys

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt  # noqa: E402

_HERE = os.path.dirname(os.path.abspath(__file__))
_REPO = os.path.dirname(os.path.dirname(_HERE))
if os.path.join(_REPO, "benchmarks") not in sys.path:
    sys.path.insert(0, os.path.join(_REPO, "benchmarks"))
import figstyle  # noqa: E402

figstyle.apply()

JSON_PATH = os.path.join(_REPO, "benchmarks", "results", "per_tier_validation.json")
OUT_DIR = os.path.join(_REPO, "outputs", "figures", "manuscript", "S6")
PAPER_DIR = os.path.join(_REPO, "paper", "figures")
STEM = "supp_fig6_per_tier_validation"

TIER_ORDER = ("high-confidence", "supported", "caller-significant-only", "not-significant")
TIER_SHORT = {
    "high-confidence": "high-\nconfidence",
    "supported": "supported",
    "caller-significant-only": "caller-sig-\nonly",
    "not-significant": "not-\nsignificant",
}
TIER_COLOR = {
    "high-confidence": "#1a7f3c",
    "supported": "#6db86d",
    "caller-significant-only": "#e3a23c",
    "not-significant": "#9aa0a6",
}
# Tiers with fewer than this many events are statistically underpowered on TRA2 (the
# 'supported' tier is n=1: reliable+effect but rMATS-non-significant is rare because
# rMATS flags almost every large effect). They are drawn muted + hatched so the bar is
# not read as a real estimate.
UNDERPOWERED_N = 5
UNDERPOWERED_FILL = "#dcdee0"
UNDERPOWERED_EDGE = "#9aa0a6"


def _save(fig) -> None:
    os.makedirs(OUT_DIR, exist_ok=True)
    os.makedirs(PAPER_DIR, exist_ok=True)
    for d in (OUT_DIR, PAPER_DIR):
        for ext in ("png", "pdf", "svg"):
            fig.savefig(os.path.join(d, f"{STEM}.{ext}"), bbox_inches="tight")


def main() -> None:
    data = json.loads(open(JSON_PATH).read())
    cf = data["interval_variants"]["cross_fit"]
    tiers = [t for t in TIER_ORDER if cf[t].get("n", 0) > 0]
    n = [cf[t]["n"] for t in tiers]
    pos = [cf[t]["rtpcr_positive_rate"] for t in tiers]
    cov = [cf[t]["rtpcr_coverage"] for t in tiers]
    colors = [TIER_COLOR[t] for t in tiers]
    labels = [TIER_SHORT[t] for t in tiers]
    base_rate = data["n_positive"] / data["n"]
    xs = list(range(len(tiers)))

    under = [ni < UNDERPOWERED_N for ni in n]

    def _mute_underpowered(bars) -> None:
        for b, u in zip(bars, under):
            if u:
                b.set_facecolor(UNDERPOWERED_FILL)
                b.set_edgecolor(UNDERPOWERED_EDGE)
                b.set_hatch("////")

    from matplotlib.patches import Patch

    underpowered_proxy = Patch(
        facecolor=UNDERPOWERED_FILL, edgecolor=UNDERPOWERED_EDGE, hatch="////",
        label=f"underpowered (n < {UNDERPOWERED_N})",
    )

    fig, (axA, axB) = plt.subplots(1, 2, figsize=(7.4, 3.4))

    _mute_underpowered(axA.bar(xs, pos, color=colors, edgecolor="#333", linewidth=0.6))
    base_line = axA.axhline(base_rate, color="#c0392b", ls="--", lw=1.0,
                            label=f"panel base rate ({base_rate:.2f})")
    for x, p, ni, u in zip(xs, pos, n, under):
        tag = f"{p:.2f}\n(n={ni})" + ("\nunderpowered" if u else "")
        axA.text(x, p + 0.02, tag, ha="center", va="bottom", fontsize=6.0,
                 color="#8a8a8a" if u else "#222")
    axA.set_ylim(0, 1.16)
    axA.set_xticks(xs)
    axA.set_xticklabels(labels, fontsize=7)
    axA.set_ylabel("RT-PCR positive rate")
    axA.set_title("A  Tiers stratify RT-PCR positivity", fontsize=9, loc="left")
    axA.legend(handles=[base_line, underpowered_proxy], fontsize=6.0,
               loc="upper right", frameon=False)

    _mute_underpowered(axB.bar(xs, cov, color=colors, edgecolor="#333", linewidth=0.6))
    nom_line = axB.axhline(0.95, color="#2c3e50", ls="--", lw=1.0, label="nominal 0.95")
    for x, c, u in zip(xs, cov, under):
        axB.text(x, c + 0.005, f"{c:.3f}", ha="center", va="bottom", fontsize=6.0,
                 color="#8a8a8a" if u else "#222")
    axB.set_ylim(0.80, 1.02)
    axB.set_xticks(xs)
    axB.set_xticklabels(labels, fontsize=7)
    axB.set_ylabel("RT-PCR coverage (cross-fit)")
    axB.set_title("B  Coverage holds within each tier", fontsize=9, loc="left")
    axB.legend(handles=[nom_line, underpowered_proxy], fontsize=6.0,
               loc="lower left", frameon=False)

    fig.tight_layout()
    _save(fig)
    plt.close(fig)
    print(f"Wrote {STEM}.(png/pdf/svg) to paper/figures/ and outputs/")


if __name__ == "__main__":
    main()
