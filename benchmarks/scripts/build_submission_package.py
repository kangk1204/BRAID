def _rel(path: Path) -> str:
    return str(path.relative_to(ROOT))


def _sha256(path: Path) -> str:
    h = hashlib.sha256()
    with path.open("rb") as handle:
        for chunk in iter(lambda: handle.read(1024 * 1024), b""):
            h.update(chunk)
    return h.hexdigest()


def _copy(src: Path, dest: Path) -> Path:
    if src.resolve() == dest.resolve():
        return src
    dest.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(src, dest)
    return dest


def _require_file(path: Path, label: str, checks: list[tuple[str, bool]]) -> None:
    checks.append((f"{label} exists", path.exists()))
    checks.append((f"{label} non-empty", path.exists() and path.stat().st_size > 0))


def _section(text: str, heading: str) -> str:
    match = re.search(rf"^## {re.escape(heading)}\s*$", text, flags=re.MULTILINE)
    if not match:
        return ""
    next_heading = re.search(r"^## ", text[match.end() :], flags=re.MULTILINE)
    if not next_heading:
        return text[match.end() :]
    return text[match.end() : match.end() + next_heading.start()]


def _abstract_body(text: str) -> str:
    return re.split(r"^Keywords:\s*", _section(text, "Abstract"), flags=re.MULTILINE)[0]


def _word_count(text: str) -> int:
    # Whitespace-delimited word count (PLOS/word-processor convention: a hyphenated
    # compound or a decimal number such as "RNA-seq" or "0.971" counts as one word).
    return len(text.split())


def _copy_figure_stem(stem: Path, dest_dir: Path, label: str) -> list[Path]:
    copied = []
    for suffix in [".png", ".pdf", ".svg"]:
        src = stem.with_suffix(suffix)
        dest = dest_dir / f"{stem.name}{suffix}"
        copied.append(_copy(src, dest))
    return copied


def _copy_figure_packages(dest_dir: Path) -> list[Path]:
    copied = []
    for package in FIGURE_PACKAGES:
        for path in sorted(package.iterdir()):
            if path.is_file() and path.suffix in {".xlsx", ".json", ".txt", ".docx", ".csv"}:
                copied.append(_copy(path, dest_dir / package.name / path.name))
    return copied


def _copy_figure_source_data(dest_dir: Path, checks: list[tuple[str, bool]]) -> list[Path]:
    """Copy the committed source-data artifact(s) for figures whose source is a
    benchmark result file rather than a self-contained figure package, so every
    rendered figure has a discoverable source-data file in the package."""
    copied = []
    for label, sources in FIGURE_SOURCE_DATA.items():
        for src in sources:
            _require_file(src, f"{label} source-data {src.name}", checks)
            if src.exists():
                copied.append(_copy(src, dest_dir / label / src.name))
    return copied


def _build_docx(source_md: Path, out_docx: Path) -> subprocess.CompletedProcess[str]:
    out_docx.parent.mkdir(parents=True, exist_ok=True)
    if out_docx.exists() and out_docx.stat().st_mtime >= source_md.stat().st_mtime:
        return subprocess.CompletedProcess(
            ["pandoc", str(source_md), "-o", str(out_docx)],
            0,
            stdout="DOCX export skipped; existing file is newer than source Markdown.",
            stderr="",
        )
    cmd = ["pandoc", str(source_md), "-o", str(out_docx)]
    # Deterministic DOCX: pin pandoc's document timestamp to a fixed epoch so the
    # rebuilt package hash is stable across runs (matches the release plan manifest).
    env = {**os.environ, "SOURCE_DATE_EPOCH": "1704067200"}  # 2024-01-01 UTC
    return subprocess.run(cmd, cwd=ROOT, text=True, capture_output=True, check=False, env=env)


def _json_safe(value: Any) -> Any:
    if isinstance(value, Path):
        return _rel(value)
    if isinstance(value, dict):
        return {str(k): _json_safe(v) for k, v in value.items()}
    if isinstance(value, (list, tuple)):
        return [_json_safe(v) for v in value]
    return value


def _write_validation(checks: list[tuple[str, bool]], path: Path) -> None:
    failed = [name for name, passed in checks if not passed]
    lines = ["BRAID PLOS One submission package validation"]
    for name, passed in checks:
        lines.append(f"{'PASS' if passed else 'FAIL'}\t{name}")
    if failed:
        lines.append("FAILED_CHECKS\t" + "; ".join(failed))
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")
    if failed:
        raise RuntimeError("Submission package validation failed: " + "; ".join(failed))


def main() -> None:
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    # Clean rebuild: clear directories that are fully repopulated below from
    # external sources so stale artifacts from a previous figure numbering cannot
    # leak into the package. (metadata/ is written in place by
    # make_plos_submission_metadata.py and consumed here; manuscript/ is left so the
    # mtime-cached DOCX export stays byte-stable across rebuilds — both are
    # overwritten in place below, so neither retains stale content.)
    for sub in ("figures", "figure_source_packages", "supplementary_data",
                "release_support"):
        target = OUT_DIR / sub
        if target.exists():
            shutil.rmtree(target)
    checks: list[tuple[str, bool]] = []
    copied: list[Path] = []

    manuscript_text = MANUSCRIPT_MD.read_text(encoding="utf-8")
    abstract_words = _word_count(_abstract_body(manuscript_text))
    checks.append(
        (
            f"canonical manuscript abstract is <= {ABSTRACT_MAX_WORDS} words",
            0 < abstract_words <= ABSTRACT_MAX_WORDS,
        )
    )
    checks.extend(
        (f"canonical manuscript excludes stale token {token}", token not in manuscript_text)
        for token in STALE_TOKENS
    )
    checks.extend(
        (f"canonical manuscript contains {needle}", needle in manuscript_text)
        for needle in REQUIRED_TEXT
    )

    package_md = _copy(MANUSCRIPT_MD, OUT_DIR / "manuscript" / "BRAID_PLOSOne_manuscript.md")
    copied.append(package_md)
    docx = OUT_DIR / "manuscript" / "BRAID_PLOSOne_manuscript.docx"
    pandoc_result = _build_docx(MANUSCRIPT_MD, docx)
    checks.append(("pandoc DOCX export exit code is zero", pandoc_result.returncode == 0))
    if pandoc_result.returncode != 0:
        (OUT_DIR / "manuscript" / "pandoc_stderr.txt").write_text(
            pandoc_result.stderr, encoding="utf-8"
        )
    copied.append(docx)

    for label, stem in MAIN_FIGURES.items():
        for suffix in [".png", ".pdf", ".svg"]:
            _require_file(stem.with_suffix(suffix), f"{label} {suffix}", checks)
        copied.extend(_copy_figure_stem(stem, OUT_DIR / "figures" / "main", label))

    for label, stem in SUPP_FIGURES.items():
        for suffix in [".png", ".pdf", ".svg"]:
            _require_file(stem.with_suffix(suffix), f"{label} {suffix}", checks)
        copied.extend(_copy_figure_stem(stem, OUT_DIR / "figures" / "supplementary", label))

    for path in SUPPLEMENTARY_DATA:
        _require_file(path, _rel(path), checks)
        copied.append(_copy(path, OUT_DIR / "supplementary_data" / path.name))

    for path in RELEASE_SUPPORT:
        _require_file(path, _rel(path), checks)
        if path.exists():
            copied.append(_copy(path, OUT_DIR / "release_support" / path.name))

    for path in SUBMISSION_METADATA_SUPPORT:
        _require_file(path, _rel(path), checks)
        if path.exists():
            copied.append(_copy(path, OUT_DIR / "metadata" / path.name))

    figure_package_copies = _copy_figure_packages(OUT_DIR / "figure_source_packages")
    copied.extend(figure_package_copies)
    copied.extend(
        _copy_figure_source_data(OUT_DIR / "figure_source_packages", checks)
    )
    for package in FIGURE_PACKAGES:
        validation = package / f"{package.name.lower()}_validation.txt"
        _require_file(validation, _rel(validation), checks)
        if validation.exists():
            text = validation.read_text(encoding="utf-8")
            checks.append((f"{_rel(validation)} has no FAIL marker", "FAIL" not in text))

    if TABLE_VALIDATION.exists():
        table_validation_text = TABLE_VALIDATION.read_text(encoding="utf-8")
        checks.append(
            ("manuscript table validation has no FAIL marker", "FAIL" not in table_validation_text)
        )
        checks.append(
            ("manuscript table validation records PASS checks", "PASS" in table_validation_text)
        )

    _require_file(docx, "DOCX manuscript", checks)
    validation = OUT_DIR / "submission_package_validation.txt"
    _write_validation(checks, validation)
    copied.append(validation)

    manifest = {
        "package_id": "BRAID-PLOSOne-local-submission-package",
        "generated_at": datetime.now(timezone.utc).isoformat(timespec="seconds"),
        "script": "benchmarks/scripts/build_submission_package.py",
        "source_manuscript": MANUSCRIPT_MD,
        "outputs": [
            {"path": _rel(path), "sha256": _sha256(path), "bytes": path.stat().st_size}
            for path in copied
            if path.exists()
        ],
        "main_figures": {label: [_rel(stem.with_suffix(s)) for s in [".png", ".pdf", ".svg"]]
                         for label, stem in MAIN_FIGURES.items()},
        "supplementary_figures": {
            label: [_rel(stem.with_suffix(s)) for s in [".png", ".pdf", ".svg"]]
            for label, stem in SUPP_FIGURES.items()
        },
        "known_limitations": [
            "This is a local submission package; the canonical public archive is the "
            "GitHub repository (https://github.com/kangk1204/BRAID). An external archive "
            "DOI is optional and recommended only for long-term citability.",
            "DOCX export is text/table oriented; figure files are supplied separately "
            "from the canonical rendered figure packages.",
            "Release/deposition plan files document the public-repository release state; "
            "an external DOI deposit is optional, not required for submission.",
        ],
    }
    manifest_path = OUT_DIR / "submission_package_manifest.json"
    manifest_path.write_text(json.dumps(_json_safe(manifest), indent=2) + "\n", encoding="utf-8")
    print(f"Wrote {_rel(package_md)}")
    print(f"Wrote {_rel(docx)}")
    print(f"Wrote {_rel(manifest_path)}")
    print(f"Wrote {_rel(validation)}")


if __name__ == "__main__":
    main()
